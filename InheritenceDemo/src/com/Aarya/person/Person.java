package com.Aarya.person;

public class Person {
	
	public String firstName;
	public String lastName;
	public int age;
	public float weight;
	public float height;
	
	public void addPerson(String firstName, String lastName, int age, float weight, float height)
	{
		this.firstName = firstName;
		this.lastName = lastName;
		this.age = age;
		this.weight = weight;
		this.height = height;
		
		
	}

}
